// DOM Selectors :
// 1. getElementsByTagName() :
// This method returns a collection of elements that matches the specified tag name.

// 2. getElementsClassName() :
// This method agains returns the collection of elements that matches the specified class name.

// 3. getElementById() :
// This method agains returns the element that matches the specified ID.
// This method is faster than the above two methods as it returns only one element.


// 4. querySelector() :
// This method returns the first element that matches the specified CSS selector.

// 5. querySelectorAll() :
// This method returns the all elements that matches the specified CSS selector.



